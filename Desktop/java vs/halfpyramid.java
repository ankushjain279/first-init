 import java.util.*;
public class halfpyramid{
    public static void main(String args[])
    {
    Scanner sc = new Scanner(System.in);
    System.out.println("input number of lines");
    int line = sc.nextInt();
    for(int i=1;i<=line;i++){
        for(int j=1;j<=i;j++){
            System.out.print(j);
    }
    System.out.print("\n");
   } 
}
}
 
    

